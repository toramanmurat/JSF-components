package _16.customconverter;

public class URLBookmark {

	String fullURL;

	public String getFullURL() {
		return fullURL;
	}

	public void setFullURL(String fullURL) {
		this.fullURL = fullURL;
	}


	public URLBookmark(String fullURL) {
		super();
		this.fullURL = fullURL;
	}

	public String toString() {
		
		return fullURL;
	}

}
